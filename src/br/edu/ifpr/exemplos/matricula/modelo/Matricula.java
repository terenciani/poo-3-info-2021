package br.edu.ifpr.exemplos.matricula.modelo;

public class Matricula {
	private int numero;

	public Matricula() {
		this.numero = 0;
	}

	public Matricula(int numero) {
		this.numero = numero;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

}
